package com.pgy.dto;

public class P_Ana {
	
	private int anaId;
	
	private String openId;
	
	private int templateId;
	
	private String anaTime;
	
	private int bindAnaId;
	
	private String bindOpenId;
	
	private String color;
	
	public int getAnaId() {
		return anaId;
	}

	public void setAnaId(int anaId) {
		this.anaId = anaId;
	}

	public String getOpenId() {
		return openId;
	}

	public void setOpenId(String openId) {
		this.openId = openId;
	}

	public int getTemplateId() {
		return templateId;
	}

	public void setTemplateId(int templateId) {
		this.templateId = templateId;
	}

	public String getAnaTime() {
		return anaTime;
	}

	public void setAnaTime(String anaTime) {
		this.anaTime = anaTime;
	}

	public int getBindAnaId() {
		return bindAnaId;
	}

	public void setBindAnaId(int bindAnaId) {
		this.bindAnaId = bindAnaId;
	}

	public String getBindOpenId() {
		return bindOpenId;
	}

	public void setBindOpenId(String bindOpenId) {
		this.bindOpenId = bindOpenId;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	
	
}
